fun main(args: Array<String>){
    var  myNumber = 10  //Int
    println(myNumber)

    var myDecimal = 1.05 // Float
    println(myDecimal)

    var myString: String // String (Mutable String)
    myString = "Hello World"
    myString = "Hello Kotlin"
    println(myString)

    val myAnotherString: String // Constant (Immutable String)
    myAnotherString = "Hi Constant"
    print(myAnotherString)

}